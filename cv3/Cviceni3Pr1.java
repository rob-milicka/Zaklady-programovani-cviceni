/*
 *	tisk minimalnich a maximalnich hodnot ciselnych
 *	datovych typu
 *
 */
 
public class Cviceni3Pr1 {
	public static void main (String[] args) {
	
		System.out.println("Min u byte = " + Byte.MIN_VALUE);
		System.out.println("Max u byte = " + Byte.MAX_VALUE);
		System.out.println("Min u short = " + Short.MIN_VALUE);
		System.out.println("Max u short = " + Short.MAX_VALUE);	
		/*	muzeme deklarovat promennou a inicializovat ji 
		 *	minimalni nebo maximalni hodnotou pro dany datovy typ
		 */
		
		int i = Integer.MIN_VALUE;
		int j = Integer.MAX_VALUE;
		
		System.out.println("Min u int = " + i);
		System.out.println("Max u int = " + j);
		
		System.out.println("Min u long = " + Long.MIN_VALUE);
		System.out.println("Max u long = " + Long.MAX_VALUE);
		System.out.println("Min u float = " + Float.MIN_VALUE);
		System.out.println("Max u float = " + Float.MAX_VALUE);
		System.out.println("Min u double = " + Double.MIN_VALUE);
		System.out.println("Max u double = " + Double.MAX_VALUE);
	}
} 



		
		
		
